let x = 8;

if(x == 5){
    console.log("x is 5");
}else if(x == 10){
    console.log("x is 10");     
}else{
    console.log("x is not 5");
}


switch(x){
    case 5:
        console.log("x is 5");
        break;
    case 10:
        console.log("x is 10");
        break;
    default:
        console.log("x is not 5");
}
